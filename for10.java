
public static void main(String[] args) {


    for (int i = 1; i <= 5; i++) {
        if (i == 3 ) {
            System.out.println("multiplo " + i);
        } else if (i % 2 == 0) {
            System.out.println("numero par: " + i);
        } else {
            System.out.println("numero impar: " + i);
        }
        
    }
}
